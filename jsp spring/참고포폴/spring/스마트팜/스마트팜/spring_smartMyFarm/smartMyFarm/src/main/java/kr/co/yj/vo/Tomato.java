package kr.co.yj.vo;

public class Tomato {
	private int no;
	private int length;
	private int height;
	private int width;
	private int leaf_len;
	private int leaf;
	private int Fruit_group;
	private int flower;
	private int fruit;
	private int week;
	private int date;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getLength() {
		return length;
	}
	public void setLength(int length) {
		this.length = length;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getLeaf_len() {
		return leaf_len;
	}
	public void setLeaf_len(int leaf_len) {
		this.leaf_len = leaf_len;
	}
	
	public int getLeaf() {
		return leaf;
	}
	public void setLeaf(int leaf) {
		this.leaf = leaf;
	}
	public int getFruit_group() {
		return Fruit_group;
	}
	public void setFruit_group(int Fruit_group) {
		this.Fruit_group = Fruit_group;
	}
	public int getFlower() {
		return flower;
	}
	public void setFlower(int flower) {
		this.flower = flower;
	}
	public int getFruit() {
		return fruit;
	}
	public void setFruit(int fruit) {
		this.fruit = fruit;
	}
	public int getWeek() {
		return week;
	}
	public void setWeek(int week) {
		this.week = week;
	}
	public int getDate() {
		return date;
	}
	public void setDate(int date) {
		this.date = date;
	}
	
}
